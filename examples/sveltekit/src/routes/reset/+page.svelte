<h1>Reset Layout Example</h1>
<p>
	This page demonstrates resetLayout functionality that clears inherited metadata from parent
	layouts.
</p>
<p>Current title should be: "Reset: Reset Layout" (not inheriting from root layout)</p>

<div>
	<h2>Reset Layout Features:</h2>
	<ul>
		<li>Clears all inherited metadata from parent layouts</li>
		<li>Starts fresh with new title template</li>
		<li>Different theme color (red instead of inherited)</li>
		<li>Different color scheme</li>
		<li>New author</li>
	</ul>
</div>

<div>
	<h2>What's Different:</h2>
