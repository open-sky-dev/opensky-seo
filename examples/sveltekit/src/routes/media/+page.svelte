<h1>Media Examples</h1>
<p>This page demonstrates all media metadata types supported by sveltekit-meta.</p>
<p>Current title should be: "Media Examples | SvelteKit Meta Examples"</p>

<div>
	<h2>Media Types Demonstrated:</h2>
	<ul>
		<li><strong>Single Image:</strong> Hero image with full metadata</li>
		<li><strong>Multiple Images:</strong> Gallery with 3 images</li>
		<li><strong>Video:</strong> Demo video with metadata</li>
		<li><strong>Content Type:</strong> largeImage for Twitter cards</li>
	</ul>
</div>

<div>
	<h2>Metadata Features:</h2>
	<ul>
		<li>Secure URLs for all media</li>
		<li>Proper MIME types</li>
		<li>Width and height dimensions</li>
		<li>Alt text for accessibility</li>
		<li>Multiple image support</li>
		<li>Video metadata</li>
	</ul>
</div>

<div>
	<h2>Generated Meta Tags:</h2>
	<ul>
		<li>og:image, twitter:image for single image</li>
		<li>Multiple og:image tags for gallery</li>
		<li>og:video, twitter:player for video</li>
		<li>twitter:card: summary_large_image</li>
		<li>All with proper dimensions and alt text</li>
	</ul>
</div>

